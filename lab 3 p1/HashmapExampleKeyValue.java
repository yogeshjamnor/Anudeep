package colleection;
import java.util.HashMap;
public class HashmapExampleKeyValue 
{
	public static void main(String[] args) 
	    {
	        // Create a HashMap
	        HashMap<String, Integer> hashMap = new HashMap<>();

	        // Add elements to the HashMap
	        hashMap.put("One", 1);
	        hashMap.put("Two", 2);
	        hashMap.put("Three", 3);

	        // Display the HashMap
	        System.out.println("HashMap: " + hashMap);
	    }
}

